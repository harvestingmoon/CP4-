import java.util.*;
public class timeloop {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int number = sc.nextInt();
        for (int i = 0; i < number; i ++) {
            int n = i+1;
            System.out.println(n + " Abracadabra");
        }
    }
}
